    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (txtNome.getText().isEmpty() || txtEmail.getText().isEmpty() || txtTelefone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
            return;
        }
        
        Cliente cliente = new Cliente();
        ClienteDAO clienteDao = new ClienteDAO();
        
        cliente.setNome(txtNome.getText());
        cliente.setEmail(txtEmail.getText());
        cliente.setTelefone(txtTelefone.getText());

        int resposta;
        Conexao conexao = new Conexao();
        boolean status = conexao.conectar();
        
        if(status == false){
            JOptionPane.showMessageDialog(null,"Erro de conexão");
        }
        else{       
            resposta = clienteDao.cadastrar(cliente);
            if (resposta == 1){
                JOptionPane.showMessageDialog(null,"Dados incluídos com sucesso!");
                txtNome.setText("");
                txtEmail.setText("");
                txtTelefone.setText("");
                txtNome.requestFocus();
            } 
            else{
                JOptionPane.showMessageDialog(null,"Erro ao tentar inserir dados");
            } 
        }
        conexao.desconectar();      
    }//GEN-LAST:event_btnCadastrarActionPerformed

